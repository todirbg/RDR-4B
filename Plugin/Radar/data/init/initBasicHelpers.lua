--- Interprets 0 value as false.
--- @param v any
--- @return any
function toboolean(v)
    if v == 0 then
        return false
    else
        return v
    end
end

-------------------------------------------------------------------------------
-------------------------------------------------------------------------------

--- Checks if 2D coordinates lays inside specified rectangle.
--- @param rect number[]
--- @param x number
--- @param y number
--- @return boolean
function isInRect(rect, x, y)
    local x1 = rect[1]
    local y1 = rect[2]
    local x2 = x1 + rect[3]
    local y2 = y1 + rect[4]
    return (x1 <= x) and (x2 > x) and (y1 <= y) and (y2 > y)
end

